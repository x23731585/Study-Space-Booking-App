    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
           // Get selected row from the table
    int selectedRow = jTable1.getSelectedRow();

    // If no row is selected
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a study space");
        return;
    }

    //read study id
    int spaceId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());

    // Read date and time from user
    String date = jComboBox1.getSelectedItem().toString();

    StudySpace selectedSpace = null;

    // Loop through all study spaces 
    for (int i = 1; i <= manager.getStudySpaces().size(); i++) {
        StudySpace space = (StudySpace) manager.getStudySpaces().get(i);

        if (space.getSpaceId() == spaceId) {
            selectedSpace = space;
            break;
        }
    }

    // If the study space was found create new booking
    if (selectedSpace != null) {
        if (!selectedSpace.isAvailable()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Study space is already booked");
                return;
    }
        Booking booking = new Booking(bookingCounter, currentStudent, selectedSpace, date);
        bookingCounter++;

       
        manager.createBooking(booking);

        
        loadAllStudySpacesToTable();
        updateBookingsDisplay();

        javax.swing.JOptionPane.showMessageDialog(this, "Your Booking Has Been Successful ");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         //cancel the most recent booking
    Booking cancelledBooking = manager.cancelLastBooking();

    // If a booking was cancelled refresh table
    if (cancelledBooking != null) {
        loadAllStudySpacesToTable();
        updateBookingsDisplay();
        javax.swing.JOptionPane.showMessageDialog(this, "Last booking cancelled");
    }
    else {
        javax.swing.JOptionPane.showMessageDialog(this, "No booking to cancel");
    }
    }//GEN-LAST:event_jButton4ActionPerformed

