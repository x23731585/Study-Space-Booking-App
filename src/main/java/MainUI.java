    // Loads all study spaces from the linked list into the table
    private void loadAllStudySpacesToTable() {

    javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) jTable1.getModel();

    // Clear old rows
    model.setRowCount(0);

    // Loop through every study space in list
    for (int i = 1; i <= manager.getStudySpaces().size(); i++) {

        StudySpace space = (StudySpace) manager.getStudySpaces().get(i);

        // Add each study space as one row in the table
        model.addRow(new Object[]{
            space.getSpaceId(),
            space.getSpaceName(),
            space.getLocation(),
            space.getCapacity(),
            space.isAvailable()
             });
         }
    }
    
    // Loads one study space only into the table
    private void loadSingleStudySpaceToTable(StudySpace space) {

     javax.swing.table.DefaultTableModel model =
             (javax.swing.table.DefaultTableModel) jTable1.getModel();

     // Clear table 
        model.setRowCount(0);

     // Only add row if the study space exists
     if (space != null) {
        model.addRow(new Object[]{
            space.getSpaceId(),
            space.getSpaceName(),
            space.getLocation(),
            space.getCapacity(),
            space.isAvailable()
           });
     }
    }
    
    
    
    
    
    

